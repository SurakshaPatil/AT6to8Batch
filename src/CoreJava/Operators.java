package CoreJava;

public class Operators {

	public static void main(String[] args) {
		int a= 10;
		int b=5;
		int sum=a+b;
		int sub=a-b;
		int mul=a*b;
		int div=a/b;
		int mod=a%b;
		System.out.println("Addition result is :"+sum);
		System.out.println("Subtraction result is: "+sub);
		System.out.println("Multiplication result is: "+ mul);
		System.out.println("Division result is: "+div);
		System.out.println("Modulus result is: "+mod);
	}

}
